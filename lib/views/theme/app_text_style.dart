import 'package:google_fonts/google_fonts.dart';

/// A class that holds the text styles used throughout the application.
///
/// The [AppTextStyles] class provides a centralized place to define and manage
/// the font family and other text styles used in the app. This helps maintain
/// consistency and makes it easier to update the styles in one place.
class AppTextStyles {
  /// The font family used for the text in the application.
  ///
  /// This font family is set using the Google Fonts package and is currently
  /// set to 'Fredoka'.
  static final String fontFamilyPrimary = GoogleFonts.fredoka().fontFamily!;
}
